const slugify = (value: string) =>
  value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");

const assetBySlug = new Map<string, MediaAsset>(
  mediaAssets.map((asset) => [asset.slug, asset]),
);

const plantSlugOverrides: Record<string, string> = {
  dragonfruit: "dragon-fruit",
  "carnivorous-plant": "carnivorous",
  tomatiro: "tomatrio",
};

const brainrotSlugOverrides: Record<string, string> = {
  garamararama: "garamararam",
};

export function getMediaBySlug(slug: string) {
  return assetBySlug.get(slug);
}

export function getPlantMedia(name: string) {
  const base = slugify(name);
  const slug = plantSlugOverrides[base] ?? base;
  return getMediaBySlug(slug);
}

export function getBrainrotMedia(name: string) {
  const base = slugify(name);
  const slug = brainrotSlugOverrides[base] ?? base;
  return getMediaBySlug(slug);
}
